export class HomeComponent implements OnInit, OnDestroy {
  slides = [
    {
      title: 'Discover Your Perfect Furniture',
      description:
        'Explore our wide range of stylish and affordable furniture to transform your home. <br> Find the best deals today!',
    },
    {
      title: 'Elevate Your Living Space',
      description:
        'Upgrade your home with our premium furniture collections. <br> Quality and style in every piece!',
    },
    {
      title: 'Shop the Latest Trends',
      description:
        'Stay ahead with the latest furniture trends. <br> Shop now and redefine your space!',
    },
  ];
  currentSlide = 0;
  isFading = false;
  private slideInterval: any;
  showBackToTop = false;
  currentYear: number = new Date().getFullYear();

  // Wishlist properties
  wishlist: boolean[] = new Array(8).fill(false); // 8 products (4 Trending + 4 Featured)

  constructor() {}

  ngOnInit(): void {
    // Start the slider auto-play
    this.startSlider();
  }

  ngOnDestroy(): void {
    // Clean up the interval to prevent memory leaks
    if (this.slideInterval) {
      clearInterval(this.slideInterval);
    }
  }

  // Slider navigation methods
  startSlider(): void {
    this.slideInterval = setInterval(() => {
      this.nextSlide();
    }, 5000); // Change slide every 5 seconds
  }

  prevSlide(): void {
    this.isFading = true;
    setTimeout(() => {
      this.currentSlide =
        (this.currentSlide - 1 + this.slides.length) % this.slides.length;
      this.isFading = false;
    }, 500); // Match the duration of the fade transition (0.5s)
  }

  nextSlide(): void {
    this.isFading = true;
    setTimeout(() => {
      this.currentSlide = (this.currentSlide + 1) % this.slides.length;
      this.isFading = false;
    }, 500); // Match the duration of the fade transition (0.5s)
  }

  // Wishlist toggle method
  toggleWishlist(index: number): void {
    this.wishlist[index] = !this.wishlist[index];
  }

  @HostListener('window:scroll', [])
  onWindowScroll(): void {
    // Show the button if the user has scrolled down more than 300px
    this.showBackToTop = window.pageYOffset > 300;
  }

  scrollToTop(): void {
    window.scrollTo({ top: 0, behavior: 'smooth' });
  }
}
